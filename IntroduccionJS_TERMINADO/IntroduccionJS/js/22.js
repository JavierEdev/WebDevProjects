const puntaje = 100;

if(puntaje === 1000) { //Igualdad en tipo de dato y tipo de valor
    console.log('Si el puntaje es 1000');
}
else {
    console.log('No es igual')
}

if(puntaje !== 1000) { //Desigualdad en tipo de dato y tipo de valor
    console.log('Si el puntaje es 1000');
}
else {
    console.log('No es igual')
}